package com.javafundamental.branching;

public class IfThenElse {
    public static void main(String[] args) {
        // if then else
        boolean isOpen = false;

        if (isOpen){
            System.out.println("Office is Open"); // aksi ketika true
        } else {
            System.out.println("Office is Closed"); // aksi ketika false
        }

    }
}
