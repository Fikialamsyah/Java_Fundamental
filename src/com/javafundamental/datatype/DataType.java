package com.javafundamental.datatype;

public class DataType {
    public static void main(String[] args) {
        // data type pada java
        // 1. byte
        byte byteNumber = 10;

        // 2. short
        short shortNumber = 2000;

        // 3. int
        int intNumber = 140;

        // 4. long
        long longNumber = 13000L;

        //  5. float
        float floatNumber = 15.f;

        // 6. double
        double doubleNumber = 5.6;

        // 7. boolean
        boolean booleanValue = true;

        // 8. char
        char charValue = 'A';


    }
}
