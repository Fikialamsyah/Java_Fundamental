package com.javafundamental.looping;

public class DoWhileLoop {
    public static void main(String[] args) {
        // do while
        int number = 1;

        do {
            System.out.println("Hello World ke-" + number); // akan melakukan 1 kali aksi ketika false
            number++; // increment or decrement
        }while (number <= 10); // cek kondisi



    }
}
